#include "stage.h"

//コンストラクタ
Stage::Stage()
	: background(new Background), map(new Map), player(new Player()),bullet(new Bullet), enemy(new Enemy())  {}
//デストラクタ
Stage::~Stage() {
	delete background;
	delete player;
	delete bullet;
	delete map;
	delete enemy;
}

