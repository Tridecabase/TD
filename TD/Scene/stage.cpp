void Stage::Render() {

	//背景の描画
	background->Render();
	//プレイヤーの描画
	player->Draw();
	//敵の描画
	enemy->Draw();
	//プレイヤーマップの描画
	map->Draw();
	//プレイヤー弾丸の描画
 	bullet->Draw();

};
